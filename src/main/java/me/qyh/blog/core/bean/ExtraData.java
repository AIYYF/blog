package me.qyh.blog.core.bean;

public final class ExtraData {
	private String key;
	private String value;

	public void setKey(String key) {
		this.key = key;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}
}